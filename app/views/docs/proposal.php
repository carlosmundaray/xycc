<h2 id="project-overview">Project Overview</h2>
<p>XY Community College requested to enhance the learning experience of their students. They’re solution is to build an online custom Learning Management System (LMS) or Course Management System (CMS).</p>

<h2 id="organization-background">Organization Background</h2>
<p>XY Community College (XYCC) selected Blackboard as its course management solution to compliment and supplement its web-based course offerings in 2012. Since that time, all online and web-enhanced courses have been hosted by Blackboard on a dedicated server, accessible from any internet ready computer. </p>
<p>Over the past few years, the adoption of Learning Management Systems (LMS) has grown and more commercial products and open source solutions have appeared on the market. Desire2Learn, Canvas, Moodle, and Sakai are a few examples of the LMS products on the forefront competing with Blackboard. They’re three year agreement with Blackboard will end in 2012. Instead of continuing their contract with BlackBoard or using another LMS solution, they decided to built a custom more enhance LMS.</p>

<h2 id="project-goals">Project Goals</h2>
<p>The goal of the project is to build a web-based Learning Management System solution which will support faculty in their teaching and enrich the student educational experience at XYCC for the next five to ten years. The LMS solution selected will also allow XYCC to remain competitive in the higher education marketplace – for all of they're course delivery options – online and on campus.</p>

